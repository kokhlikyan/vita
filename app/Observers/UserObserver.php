class UserObserver
{
    /**
     * Handle the User "created" event.
     */
    public function created(User $user): void
    {
        $settings = Settings::query()->where('user_id', $user->id)->first();
        if (!$settings) {
            Settings::query()->create([
                'user_id' => $user->id
            ]);
            Log::info('Settings created for user with ID: ' . $user->id);
        }
    }

